export class Scaffolding {

	/**
	 * The distance from a center scaffolding horizontally. If it is 7, it will turn into a falling block entity.
	 * Allowed values: 0-7.
	 * @default 0.
	 */
	stability: number = 7;

	/**
	 * If this scaffolding is floating.
	 * Allowed values: 0-1.
	 * @default 0.
	 */
	stability_check: number = 0;
}
